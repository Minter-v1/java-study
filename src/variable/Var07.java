package variable;

public class Var07 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.0; // 실수
        char c = 'c';
        boolean d = false;
        String e = "e";

        // NOTE: 같은 패키지 내에서, 오류가 있는 소스코드가 있으면 컴파일 에러남
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);


    }
}
