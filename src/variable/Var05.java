package variable;

public class Var05 {
    public static void main(String[] args) {
        int a;
        a = 1;
        System.out.println(a);

        // MARK: 변수 선언, 초기화 동시에
        int b = 2;
        System.out.println(b);

        // MARK: 변수 여러 개 선언 및 초기화
        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);

    }
}
